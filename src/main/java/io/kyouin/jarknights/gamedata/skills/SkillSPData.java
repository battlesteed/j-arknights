package io.kyouin.jarknights.gamedata.skills;

public class SkillSPData {

    private Integer spType;
    private Integer maxChargeTime;
    private Integer spCost;
    private Integer initSp;
    private Float increment;

    public Integer getType() {
        return spType;
    }

    public Integer getMaxChargeTime() {
        return maxChargeTime;
    }

    public Integer getSPCost() {
        return spCost;
    }

    public Integer getInitSP() {
        return initSp;
    }

    public Float getIncrement() {
        return increment;
    }
}
