package io.kyouin.jarknights.gamedata.handbooks;

public class StoryText {

    private String storyText;
    private Integer unLockType;
    private String unLockParam;
    private String unLockString;

    public String getText() {
        return storyText;
    }

    public Integer getUnlockType() {
        return unLockType;
    }

    public String getUnlockParam() {
        return unLockParam;
    }

    public String getUnlockString() {
        return unLockString;
    }
}
